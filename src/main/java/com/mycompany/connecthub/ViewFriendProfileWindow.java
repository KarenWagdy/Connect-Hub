    public ViewFriendProfileWindow(String username) {
        initComponents();
        this.viewedUser=Functionalities.getUser(username);
        posts= PostDatabase.readPostsforUser(viewedUser.getUserId());
        friends=FriendDatabase.readFriends(viewedUser.getUserId());
        stories=StoryDatabase.readStoriesforUser(viewedUser.getUserId());
        users=UsersDatabase.readUsers();
         LoadPosts();
         LoadFriends();
         LoadStories();
         Loadpfp();
         LoadcoverPhoto();
         LoadBio();
         LoadName();
        
    }
    
    public void LoadName()
     {   
             String Name=viewedUser.getUsername();
             NameLabel.setText(Name);
}
    
    public void LoadPosts() {
        //view posts in postsList
        DefaultListModel<String> listModel = new DefaultListModel<>();
        
        for (Post post : posts) {
            listModel.addElement(post.getContent());
        }
         postsList.setModel(listModel);
    }

    public void LoadFriends() {
        //view friends in FriendList
        DefaultListModel<String> listModel = new DefaultListModel<>();

        for (User friend : friends) {
            listModel.addElement(friend.getUsername() + " ("+friend.getStatus()+") ");
        }
         FriendsList.setModel(listModel);

    }
     public void LoadStories() {
        //view posts in postsList
        DefaultListModel<String> listModel = new DefaultListModel<>();
        StoryDatabase.deleteStories();
        for (Story story : stories) {
            listModel.addElement(story.getContent());
            
        }
        storiesList.setModel(listModel);
        
    }
     
     public void Loadpfp()
     {
         String filepath=null;
         for(int i=0;i<users.size();i++)
         {
             if(users.get(i).getUserId()==viewedUser.getUserId())
             {
                  filepath=users.get(i).getProfilePicture();
             }
         }
         
         if(filepath!=null)
         {
           ImageIcon profilePicture = new ImageIcon(filepath);
    Image pfp = profilePicture.getImage();
    Image scaledPFP = pfp.getScaledInstance(300, 200, Image.SCALE_SMOOTH);
    ImageIcon scaledIcon = new ImageIcon(scaledPFP);
    pfpJLabel.setIcon(scaledIcon);
         }
         
         
     }
     public void LoadcoverPhoto()
     {
         String filepath=null;
         for(int i=0;i<users.size();i++)
         {
             if(users.get(i).getUserId()==viewedUser.getUserId())
             {
                  filepath=users.get(i).getCoverPicture();
             }
         }
         
         if(filepath!=null)
         {
           ImageIcon coverPhoto = new ImageIcon(filepath);
    Image cover = coverPhoto.getImage();
    Image scaledPFP = cover.getScaledInstance(300, 200, Image.SCALE_SMOOTH);
    ImageIcon scaledIcon = new ImageIcon(scaledPFP);
    coverJLabel.setIcon(scaledIcon);
         }
         
         
     }
     
     public void LoadBio()
     {
         String newBio=null;
         for(int i=0;i<users.size();i++)
         {
             if(users.get(i).getUserId()==viewedUser.getUserId())
             {
                  newBio=users.get(i).getBio();
             }
         }
         if(newBio.equals("null"))
         {
             bioText.setText("");
         }
         else{
         bioText.setText(newBio);
         }
         
     }
     


